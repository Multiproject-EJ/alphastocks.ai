// Helper functions
export function getAchievementById(id: string): Achievement | undefined {
  return ACHIEVEMENTS.find(a => a.id === id)
}

export function getAchievementsByCategory(category: Achievement['category']): Achievement[] {
  return ACHIEVEMENTS.filter(a => a.category === category)
}

export function getVisibleAchievements(unlockedIds: string[]): Achievement[] {
  return ACHIEVEMENTS.filter(a => {
    if (a.isSecret && !unlockedIds.includes(a.id)) return false
    return true
  })
}

export function getTotalAchievementRewards(): number {
  return ACHIEVEMENTS.reduce((sum, a) => sum + a.reward, 0)
}
